export function pricingLabel(value, lang='sk') {
  const raw=String(value||'').toLowerCase().trim();
  const isMonthly=['monthly','month','mesiac','mesac','tháng','thang'].includes(raw);
  if(lang==='vi') return isMonthly ? 'tháng' : 'ngày';
  if(lang==='en') return isMonthly ? 'month' : 'day';
  return isMonthly ? 'mesiac' : 'deň';
}
