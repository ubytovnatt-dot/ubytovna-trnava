function escapeRegExp(string) {
  return String(string).replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function translateText(text, lang) {
  if (!lang || lang === 'sk') return text;
  const dict = dictionaries[lang] || {};
  if (!text || !String(text).trim()) return text;
  let out = String(text);
  if (dict[out]) return dict[out];
  Object.entries(dict).sort((a,b)=>b[0].length-a[0].length).forEach(([from,to])=>{
    out = out.replace(new RegExp(escapeRegExp(from),'g'), to);
  });
  return out;
}

let __stayhubTranslatedOnce = false;
export function translateDom(lang) {
  if (typeof document === 'undefined') return;
  document.documentElement.lang = lang || 'sk';
  // v6.4.9 vykon: pri slovencine (default) preskoc prehladavanie DOM, ak sme nikdy neprekladali
  // (niet co prekladat ani obnovovat). Prekladame az ked sa prepne na iny jazyk.
  if ((lang || 'sk') === 'sk' && !__stayhubTranslatedOnce) return;
  if (lang && lang !== 'sk') __stayhubTranslatedOnce = true;
  const root = document.getElementById('root');
  if (!root) return;
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      const parent = node.parentElement;
      if (!parent) return NodeFilter.FILTER_REJECT;
      if (['SCRIPT','STYLE','TEXTAREA','INPUT'].includes(parent.tagName)) return NodeFilter.FILTER_REJECT;
      const text = node.nodeValue || '';
      if (!text.trim()) return NodeFilter.FILTER_REJECT;
      // v6.4.4: neprekladaj/necachuj cisto ciselne uzly (ceny, pocty, %),
      // inak translateDom prepise Reactom aktualizovane dynamicke hodnoty spat na povodne.
      if (!/[A-Za-z\u00C0-\u024F]/.test(text)) return NodeFilter.FILTER_REJECT;
      return NodeFilter.FILTER_ACCEPT;
    }
  });
  const nodes=[];
  while(walker.nextNode()) nodes.push(walker.currentNode);
  nodes.forEach(node=>{
    const sk=node.__skText || node.nodeValue;
    node.__skText=sk;
    node.nodeValue=translateText(sk, lang);
  });
  root.querySelectorAll('option').forEach(el=>{
    const sk=el.__skText || el.textContent;
    el.__skText=sk;
    el.textContent=translateText(sk, lang);
  });
  root.querySelectorAll('[title], [aria-label]').forEach(el=>{
    ['title','aria-label'].forEach(attr=>{
      if(!el.hasAttribute(attr)) return;
      const key='__sk_'+attr;
      const sk=el[key] || el.getAttribute(attr);
      el[key]=sk;
      el.setAttribute(attr, translateText(sk, lang));
    });
  });
  root.querySelectorAll('input[placeholder], textarea[placeholder]').forEach(el=>{
    const sk=el.__skPlaceholder || el.getAttribute('placeholder');
    el.__skPlaceholder=sk;
    el.setAttribute('placeholder', translateText(sk, lang));
  });
}

